package channels

import "anyclaw/pkg/sdk"

// Channel is the internal alias for the public channel contract.
type Channel = sdk.Channel

// ChannelRegistry is the internal alias for the public channel registry contract.
type ChannelRegistry = sdk.ChannelRegistry

// InboundMessage is the normalized inbound message shape.
type InboundMessage = sdk.InboundMessage

// OutboundMessage is the normalized outbound message shape.
type OutboundMessage = sdk.OutboundMessage
